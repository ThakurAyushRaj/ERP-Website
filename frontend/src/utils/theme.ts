export type ThemeKey = keyof typeof THEMES;

const STORAGE_KEY_THEME = "erp_dashboard_theme";

export const getStoredTheme = (): ThemeKey => {
  try {
    const stored = localStorage.getItem(STORAGE_KEY_THEME);
    if (stored && stored in THEMES) return stored as ThemeKey;
  } catch { /* ignore */ }
  return "default";
};

export const saveTheme = (themeKey: ThemeKey) => {
  try {
    localStorage.setItem(STORAGE_KEY_THEME, themeKey);
  } catch { /* ignore */ }
};
